				<strong>Instructions:</strong><br/>
				<br/>
				This page will allow you to create new feeds to be monitored and sent by the plugin.  Simply enter
				the required details about the feed.<br/>
				<br/>
				<strong>Email Templates</strong>
					You may customize the following TEXT and HTML templates to suit how you wish the email to be displayed.
					The following MERGE VARIABLES are supported.  Anywhere you place these variables will be replaced by the
					appropriate values in the XML feed.<br/>
					<ul>
						<li>&lt;SITE_LINK&gt; - Main Website URL</li>
						<li>&lt;SITE_TITLE&gt; - Main Website Title</li>
						<li>&lt;ARTICLE_AUTHOR&gt; - Article Author</li>
						<li>&lt;ARTICLE_LINK&gt; - Article URL</li>
						<li>&lt;ARTICLE_COMMENTS&gt; - Article Comments URL</li>
						<li>&lt;ARTICLE_TITLE&gt; - Article Title</li>
						<li>&lt;ARTICLE_DESCRIPTION&gt; - Article Teaser/Description</li>
						<li>&lt;ARTICLE_CONTENT&gt; - Article Content</li>
						<li>&lt;NICEDATE&gt; - Nicely formatted date stamp in the form of: March 10th, 2008</li>
						<li>&lt;DATESTAMP&gt; - Datestamp in the form of: mm/dd/yyyy</li>
						<li>&lt;TIMESTAMP&gt; - Timestamp in the form of: 5:32 pm</li>
						<li>The following can also be used in the same way as their counterparts above, but are useful
						for indicating which recent post number (1-10) you wish to display data for:<br/>
						&lt;ARTICLE_LINK_#&gt;<br/>
						&lt;ARTICLE_COMMENTS_#&gt;<br/>
						&lt;ARTICLE_TITLE_#&gt;<br/>
						&lt;ARTICLE_DESCRIPTION_#&gt;<br/>
						&lt;ARTICLE_CONTENT_#&gt;<br/>
						You would replace # with the number of the feed item you wish to refer to.<br/>
						(ie. &lt;ARTICLE_COMMENTS_5&gt;)
						</li>
					</ul>
			</div>		
